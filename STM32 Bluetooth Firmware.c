/*
* Recieve Interupt Function
* This function is called on successful reception of data
*/

void HAL_UART_RxCpltCallback (UART_HandleTypeDef *huart)
{
    if(huart1.Instance == USART1)
		{
         buffer[i] = ch;               // filling 'buffer', index by index, by 'ch', which has been received from main()
          
			     // Check what is sent, and glow the LED
			     if((uint8_t)'1' == ch)
					 {
						 HAL_GPIO_WritePin(GPIOB, LED_Pin,GPIO_PIN_SET);
					 }
					 else if((uint8_t)'0' == ch)
					 {
						 HAL_GPIO_WritePin(GPIOB, LED_Pin,GPIO_PIN_RESET);
					 }
			
         i++;

         HAL_UART_Receive_IT(&huart1, (uint8_t*)&ch, 1);
    }     	
}

