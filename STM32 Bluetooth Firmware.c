// Global variables
uint8_t buffer[100];  // Buffer to store UART received data
uint8_t ch;           // Variable to store 1 byte recieved data
uint32_t i = 0;       // Variable to Buffer increment

UART_HandleTypeDef huart1;        // UART handler structure

/* Function Prototypes */
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART1_UART_Init(void);
void HAL_UART_RxCpltCallback (UART_HandleTypeDef *huart);

int main(void)
{

  HAL_Init();              // HAL drivers initailization

  SystemClock_Config();    // System clock initialization

  MX_GPIO_Init();          // GPIO pins initialization
  MX_USART1_UART_Init();   // UART driver initialzation
	
	// Entering Forever loop
  while (1)
  {
      HAL_UART_Receive_IT(&huart1, (uint8_t*)&ch,  1);   // Driver function to UART receive with Interrupt
  }

}

